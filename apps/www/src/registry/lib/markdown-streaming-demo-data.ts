function splitTextIntoChunks(
  source: string,
  pattern: readonly number[]
): string[] {
  const chunks: string[] = [];
  let index = 0;
  let patternIndex = 0;

  while (index < source.length) {
    const size = pattern[patternIndex % pattern.length]!;
    chunks.push(source.slice(index, index + size));
    index += size;
    patternIndex += 1;
  }

  return chunks;
}

