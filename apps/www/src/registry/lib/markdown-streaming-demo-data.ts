export function getPlaybackDelayInMs(
  playbackDelayInMs: number,
  chunkDelayInMs?: number | null
) {
  return Math.max(playbackDelayInMs, chunkDelayInMs ?? 0);
}

export function getNextPlaybackIndex(
  currentIndex: number,
  totalChunks: number,
  playbackBurstSize: number
) {
  return Math.min(totalChunks, currentIndex + playbackBurstSize);
}
